package day06_operators2;

public class IncrementDecrement {
	public static void main(String[]args) {
		int employees = 50;
		
		employees++;//added 1
		employees++;//same
		employees+=1;//same
		employees= employees+1;//same
	
		System.out.println(employees);
		
		employees--;//minus 1
		System.out.println(employees);
		
		
		
		
		int apples=5;
		apples = apples + 1;
		apples+=1;
		apples++;
		
		
	}

}
